// ─── Utility helpers ────────────────────────────────────────────────────────
const _avg = (arr) => arr.reduce((a, b) => a + b, 0) / arr.length;
const _r1 = (n) => Math.round(n * 10) / 10;

export const MONTHS = [
  { code: "2024-06", name: "June" },
  { code: "2024-07", name: "July" },
  { code: "2024-08", name: "August" },
  { code: "2024-09", name: "September" },
];

export function getOverallStats(data = weatherData) {
  const temps = data.map((d) => d.temperature);
  const winds = data.map((d) => d.wind_speed);
  const humids = data.map((d) => d.humidity);
  const precips = data.map((d) => d.precipitation);
  const maxTempIdx = temps.indexOf(Math.max(...temps));
  const minTempIdx = temps.indexOf(Math.min(...temps));
  const maxWindIdx = winds.indexOf(Math.max(...winds));
  const maxPrecipIdx = precips.indexOf(Math.max(...precips));
  return {
    totalDays: data.length,
    avgTemp: _r1(_avg(temps)),
    maxTemp: Math.max(...temps),
    minTemp: Math.min(...temps),
    maxTempDay: data[maxTempIdx].date,
    minTempDay: data[minTempIdx].date,
    avgHumidity: _r1(_avg(humids)),
    totalPrecip: _r1(precips.reduce((a, b) => a + b, 0)),
    rainyDays: data.filter((d) => d.precipitation > 0).length,
    maxPrecip: _r1(Math.max(...precips)),
    maxPrecipDay: data[maxPrecipIdx].date,
    avgWind: _r1(_avg(winds)),
    maxWind: Math.max(...winds),
    maxWindDay: data[maxWindIdx].date,
  };
}

export function getMonthlyStats(data = weatherData) {
  return MONTHS.map(({ code, name }) => {
    const md = data.filter((d) => d.date.startsWith(code));
    if (!md.length) return null;
    const temps = md.map((d) => d.temperature);
    const winds = md.map((d) => d.wind_speed);
    const humids = md.map((d) => d.humidity);
    const precips = md.map((d) => d.precipitation);
    return {
      month: name,
      code,
      days: md.length,
      avgTemp: _r1(_avg(temps)),
      maxTemp: _r1(Math.max(...temps)),
      minTemp: _r1(Math.min(...temps)),
      avgHumidity: _r1(_avg(humids)),
      totalPrecip: _r1(precips.reduce((a, b) => a + b, 0)),
      rainyDays: md.filter((d) => d.precipitation > 0).length,
      avgWind: _r1(_avg(winds)),
      maxWind: _r1(Math.max(...winds)),
    };
  }).filter(Boolean);
}

export function getTempDistribution(data = weatherData) {
  const bins = [
    { label: "<22°", min: -Infinity, max: 22 },
    { label: "22-24°", min: 22, max: 24 },
    { label: "24-26°", min: 24, max: 26 },
    { label: "26-28°", min: 26, max: 28 },
    { label: "28-30°", min: 28, max: 30 },
    { label: ">30°", min: 30, max: Infinity },
  ];
  return bins.map((b) => ({
    label: b.label,
    count: data.filter((d) => d.temperature >= b.min && d.temperature < b.max)
      .length,
  }));
}

